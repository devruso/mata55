package lista_metodos_e_campos_estaticos.exercicio2;

public class ConversaoDeUnidadesDeVolume {

    static double litroParaCentimetroCubico(int n){
        return n * 1000;
    }

    static double metroCubicoParaLitro(int n){
        return n * 1000;
    }

    static double metroCubicoParaPesCubico(int n){
        return n * 35.32;
    }

    static double galaoAmericanoParaPolegadaCubica(int n){
        return n * 231;
    }

    static double galaoAmericanoParaLitros(int n){
        return n * 3785;
    }

}

package lista_metodos_e_campos_estaticos.exercicio5;

public class ConversaoDeUnidadesDeTempo {

    static int minutoParaSegundo(int n){
        return n * 60;
    }

    static int horaParaMinuto(int n){
        return n * 60;
    }

    static int diaParaHora(int n){
        return n * 24;
    }

    static int diaParaMinuto(int n){
       return n * 1440;
    }

    static int diaParaSegundo(int n){
        return n * 86400;
    }

    static int semanaParaDia(int n){
        return n * 7;
    }

    static int mesParaDia(int n){
        return n * 30;
    }

    static double anoParaDia(int n){
        return n * 365.25;
    }


}
